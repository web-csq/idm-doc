<h2 id="什么是idm" tabindex="-1"><a class="header-anchor" href="#什么是idm" aria-hidden="true">#</a> 什么是IDM？</h2>
<p>IDM，我有一个梦想（I Dream）的英文简写，是一个基于<a href="https://cn.vuejs.org/" target="_blank" rel="noopener noreferrer">Vue<ExternalLinkIcon/></a>实现的低（零）代码可视化拖拽前端框架。在当下时代您还在逐个页面单独开发吗？一个好用的组件标准化可视拖拽框架是必不可少的，希望IDM能成为您的选择。</p>
<h3 id="初衷" tabindex="-1"><a class="header-anchor" href="#初衷" aria-hidden="true">#</a> 初衷</h3>
<p>IDM诞生的初衷是为了尽可能的减少代码、业务功能组件的编写，从而实现组件的复用，通过一系列的属性进行对组件控制，实现尽可能的不去修改代码来实现需求的变更。</p>
<p>同时为了任何项目、系统、框架都能用上IDM，因此IDM选择了前端独立化，通过定义标准接口实现前后端分离，集成IDM只需要实现后端接口即可，目前已有spring mvc框架的后端源码。</p>
<h3 id="奋斗目标" tabindex="-1"><a class="header-anchor" href="#奋斗目标" aria-hidden="true">#</a> 奋斗目标</h3>
<p>IDM最终目标是上云平台，让人人开发者都能实现组件共享共同维护，同时也有志成为一个真正的智能零代码平台。</p>
<h2 id="核心设计理念" tabindex="-1"><a class="header-anchor" href="#核心设计理念" aria-hidden="true">#</a> 核心设计理念</h2>
<ul>
<li>🥄 简单</li>
</ul>
<p>IDM核心框架对于组件开发者而言无非就是组件与组件、组件与核心框架之间的沟通桥梁，所以开发一个组件只需要新建属性文件和组件存放代码文件即可完成一个组件的编写。而对于使用者来说，只需要会基本拖拽、组件属性理解能力即可完成页面的搭建，由组件构建成页面，再由页面构建成应用，整个过程是一个非常简单的事情。</p>
<ul>
<li>🍡 解耦/技术栈无关</li>
</ul>
<p>IDM的核心目标是将一切可视页面都组件化，将页面拆分成可以自治的松耦合标准化组件，由于核心框架与组件之间的关系建立标准化，所以能做到与技术栈无关系。任何组件开发者只要用自己熟悉的技术栈基于IDM提供的脚手架来构建就能实现组件的开发，</p>
