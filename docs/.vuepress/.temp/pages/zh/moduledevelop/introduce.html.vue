<h2 id="什么是组件开发" tabindex="-1"><a class="header-anchor" href="#什么是组件开发" aria-hidden="true">#</a> 什么是组件开发？</h2>
<ul>
<li>
<p><strong>在传统概念中</strong></p>
<p>组件化开发即是对某些可以进行复用的功能进行封装的标准化工作。组件一般会内含他的内部UI元素、样式和JS逻辑代码，它可以很方便的在应用的任何地方进行快速的嵌入。组件内部可以使用其他组件来构成更复杂的组件。</p>
</li>
<li>
<p><strong>在IDM框架中</strong></p>
<p>IDM只是提供了组件拖拽、页面渲染、组件文档管理、组件联动等等功能的一个标准化框架。因此组件的代码是与IDM框架分离的，IDM不约束您使用任何技术栈，只要您遵守IDM定制的组件开发标准，你可以使用Vue、React、JQuery、Angular、甚至直接使用JavaScript或未来的技术栈也是可以的。</p>
<p>同时IDM支持组件的代码包按需加载，支持多种技术栈开发的组件同时使用。但它是不内置任何组件，比如常用的按钮、文本、图片组件也没有。但如果您不想重复开发组件的话，您可以前往『 <a href="http://www.yunit.cn" target="_blank" rel="noopener noreferrer">云IT<ExternalLinkIcon/></a> 』组件共享平台获取您想要的组件。</p>
</li>
</ul>
<h2 id="特性" tabindex="-1"><a class="header-anchor" href="#特性" aria-hidden="true">#</a> 特性</h2>
<ul>
<li>⚡️ <strong>简单快速</strong>，只需要两个文件即可完成一个新的组件开发。</li>
<li>📱 <strong>技术栈无关</strong>，任意技术栈开发的组件均可接入，不论是React/Vue/Angular/JQuery还是其他技术栈。</li>
<li>👌 <strong>零代码接入</strong>，让您接入组件到IDM中就像傻瓜式安装一个桌面APP一样简单。</li>
<li>💥 <strong>标准化自由开发</strong>，基于标准实现高度自由开发组件。</li>
<li>✊ <strong>组件独立</strong>，让您的组件既可以单独运行也能在IDM中与其他组件联合使用。</li>
<li>💠 <strong>丰富API</strong>，让你在开发组件的时候尽量的少些重复的代码。</li>
<li>📋 <strong>标准化文档</strong>，让你的组件使用起来更加简单。</li>
<li>🤝 <strong>组件通信</strong>，让你与任何人开发的组件都能进行沟通。</li>
</ul>
